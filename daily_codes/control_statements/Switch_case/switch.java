class Switch{
	public static void main(String[] args){
		int num=3;
		System.out.println("before switch");
		switch(num){
			case 1:
				System.out.println("One");
			case 2:
				System.out.println("Two");
			case 3:
				System.out.println("Three");
		}
		System.out.println("after switch");
	}
}
// before switch
// Three
// after switch
