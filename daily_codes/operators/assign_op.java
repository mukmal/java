class assign{
	public static void main(String[] args){
		int a=10;
		int b=5;
		a+=b; //a=a+b
		System.out.println(a);		//15
		System.out.println(b);		//5
		int p=10;
		int q=5;
		p-=q;	//p=p-q
		System.out.println(p);		//5
		System.out.println(q);		//5
		int x=10;
		int y=5;
		x/=y;	//x=x/y
		System.out.println(x);		//2
		System.out.println(y);		//5
	}
}
