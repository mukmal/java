import java.util.Scanner;
class Demo{
	public static void main(String[] args){
		Scanner sc= new Scanner(System.in);
		System.out.print("Enter Array Size : ");
		int size=sc.nextInt();
		int arr[] = new int[size];
		System.out.println("Array Size : "+arr.length);
	}
}

