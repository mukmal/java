class For{
	public static void main(String[] args){
		for(int i=1;i<=4;i++){			//for 4 row i<=4
			for(int j=1;j<=4;j++){		//	    j<=4
				System.out.print("$#	");
			}
			System.out.println();
		}
	}
}
